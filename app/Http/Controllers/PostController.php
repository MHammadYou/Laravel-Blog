     */
    public function show($id)
    {
        $post = Post::where('id', $id)->first();
        if ($post != null) {

            $userId = $post->user_id;
            $user = User::where('id', $userId)->first();

            $data = [
                'post' => $post,
                'user' => $user
            ];
            return view('posts/post', $data);
        } else {
            echo "No such post";
        }
    }

